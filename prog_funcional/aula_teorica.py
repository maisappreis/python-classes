
# Programação Funcional
# Consome mais memória e processador.

# First Class Functions: armazenar uma Função dentro de uma variável. Funções como dados.
# High Order Functions: funções que recebem outras Funções como parametro. Ou Função retornando Função.
# Anonymous Functions: as 'Lambda'.
# Closure: a função sabe o que tem dentro dela, e tmb tem conciência do que tem ao redor dela.
# Recursion: recursividade, uma função que se auto chama.
# Immutability: dados imutáveis.
# Lazy Evaluation: avaliação tardia. Deixar o processamento para quando for realmente necessário. Como no generator, que gera conforme demanda.

# ZIP
